def print_topologies(data):
    from pathlib import Path
    topology_TOP = set()
    topology_SNT = set()
    topology_SNB = set()
    topology_OUT = set()
    topology_MAR = set()
    topology_IN = set()
    topology_DN = set()
    topology_BOT = set()

    if type(data) is not dict:
        print('Loading')
        abs_path = Path(data).resolve()
        if abs_path.exists():
            with open(abs_path, 'rb') as f:
                data = pickle.load(f, encoding='latin1')
        else:
            print(abs_path)
            raise IOError("No such path to data file")

    for shot in tqdm(data.keys()):
        if 'TOP' in str(data[shot]['topology']):
            topology_TOP.add(shot)
        if 'SNT' in str(data[shot]['topology']):
            topology_SNT.add(shot)
        if 'SNB' in str(data[shot]['topology']):
            topology_SNB.add(shot)
        if 'OUT' in str(data[shot]['topology']):
            topology_OUT.add(shot)
        if 'MAR' in str(data[shot]['topology']):
            topology_MAR.add(shot)
        if 'IN' in str(data[shot]['topology']):
            topology_IN.add(shot)
        if 'DN' in str(data[shot]['topology']):
            topology_DN.add(shot)
        if 'BOT' in str(data[shot]['topology']):
            topology_BOT.add(shot)

    def print_var(var, name):
        s = name + ' = {'
        for i, elem in enumerate(var):
            s += str(elem) + ', '
            if (i+1)%10==0:
                s += '\n' + ' '*(len(name)+4)
        s += '}\n'
        print(s)

    print_var(topology_SNT,'topology_SNT')
    print_var(topology_SNB,'topology_SNB')
    print_var(topology_TOP,'topology_TOP')
    print_var(topology_OUT,'topology_OUT')
    print_var(topology_MAR,'topology_MAR')
    print_var(topology_IN,'topology_IN')
    print_var(topology_DN,'topology_DN')
    print_var(topology_BOT,'topology_BOT')
